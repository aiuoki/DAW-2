/**
 * Módulo de la calculadora
 * @module calculadora
 */

/**
 * Suma dos números
 * @param {number} num1 - Primer número
 * @param {number} num2 - Segundo número
 * @returns {number} - Resultado de la suma
 */
exports.sumar = (num1, num2) => num1 + num2;

/**
 * Resta dos números
 * @param {number} num1 - Primer número
 * @param {number} num2 - Segundo número
 * @returns {number} - Resultado de la resta
 */
exports.restar = (num1, num2) => num1 - num2;

/**
 * Multiplica dos números
 * @param {number} num1 - Primer número
 * @param {number} num2 - Segundo número
 * @returns {number} - Resultado de la multiplicación
 */
exports.multiplicar = (num1, num2) => num1 * num2;

/**
 * Divide dos números
 * @param {number} num1 - Primer número
 * @param {number} num2 - Segundo número
 * @returns {number} - Resultado de la división
 */
exports.dividir = (num1, num2) => num1 / num2;